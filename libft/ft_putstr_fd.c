#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	x;

	x = 0;
	if (fd >= 0 && s)
	{
		while (s[x])
		{
			write(fd, &s[x], 1);
			x++;
		}
	}
}
